#!/usr/bin/python3
def add(a, b):
    """the addition function to use

    Args:
        a: the first integer
        b: the second integer

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """the subtraction function to use

    Args:
        a: the first integer
        b: the second integer

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """the multiplication function to use

    Args:
        a: the first integer
        b: the second integer

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """the division function to use

    Args:
        a: the first integer
        b: the second integer

    Returns:
        The return value. a / b
    """
    return int(a / b)
